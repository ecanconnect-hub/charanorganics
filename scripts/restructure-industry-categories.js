async function main(){
  const env=loadEnv();
  const supabase=createClient(env.NEXT_PUBLIC_SUPABASE_URL,env.SUPABASE_SERVICE_ROLE_KEY,{auth:{persistSession:false,autoRefreshToken:false}});

  // Upsert industry sections
  const sectionPayload=sections.map(s=>({
    section_id:s.section_id,title_en:s.title_en,title_te:s.title_te,subtitle_en:s.subtitle_en,subtitle_te:s.subtitle_te,
    description_en:s.description_en,description_te:s.description_te,display_order:s.display_order,is_enabled:true
  }));
  let {error}=await supabase.from('sections').upsert(sectionPayload,{onConflict:'section_id'});
  if(error) throw error;

  // Disable all other sections for clean UX
  const keep=sections.map(s=>s.section_id);
  ({error}=await supabase.from('sections').update({is_enabled:false,updated_at:new Date().toISOString()}).not('section_id','in',`(${keep.map(x=>`"${x}"`).join(',')})`));
  if(error) throw error;

  // fetch sections + products
  const {data:dbSections,error:secErr}=await supabase.from('sections').select('id,section_id').in('section_id',keep);
  if(secErr) throw secErr;
  const secId=new Map(dbSections.map(s=>[s.section_id,s.id]));

  const allProductCodes=[...new Set(Object.values(mapping).flat())];
  const {data:dbProducts,error:prodErr}=await supabase.from('products').select('id,product_id,image_url,is_active').in('product_id',allProductCodes);
  if(prodErr) throw prodErr;

  const prodId=new Map(dbProducts.map(p=>[p.product_id,p.id]));
  const imageByProduct=new Map(dbProducts.filter(p=>p.image_url).map(p=>[p.product_id,p.image_url]));

  // activate mapped products (avoid empty categories)
  const activateUuids=dbProducts.map(p=>p.id);
  if(activateUuids.length){
    ({error}=await supabase.from('products').update({is_active:true,updated_at:new Date().toISOString()}).in('id',activateUuids));
    if(error) throw error;
  }

  // clear mapping for kept sections and reinsert clean
  const targetSectionUuids=dbSections.map(s=>s.id);
  ({error}=await supabase.from('product_sections').delete().in('section_id',targetSectionUuids));
  if(error) throw error;

  const inserts=[];
  for(const [sectionSlug,codes] of Object.entries(mapping)){
    const sid=secId.get(sectionSlug);
    if(!sid) continue;
    let order=1;
    for(const code of codes){
      const pid=prodId.get(code);
      if(!pid) continue;
      inserts.push({product_id:pid,section_id:sid,display_order:order++});
    }
  }

  if(inserts.length){
    ({error}=await supabase.from('product_sections').upsert(inserts,{onConflict:'product_id,section_id'}));
    if(error) throw error;
  }

  // set section images
  for(const s of sections){
    const img=imageByProduct.get(s.image_product_id);
    if(!img) continue;
    ({error}=await supabase.from('sections').update({image_url:img,updated_at:new Date().toISOString()}).eq('section_id',s.section_id));
    if(error) throw error;
  }

  // final counts
  const {data:activeProducts}=await supabase.from('products').select('id').eq('is_active',true);
  const activeSet=new Set((activeProducts||[]).map(p=>p.id));
  const {data:ps}=await supabase.from('product_sections').select('product_id,section_id').in('section_id',targetSectionUuids);
  const countBySection={};
  for(const r of (ps||[])){if(!activeSet.has(r.product_id)) continue; countBySection[r.section_id]=(countBySection[r.section_id]||0)+1;}

  const summary=dbSections.map(s=>({section_id:s.section_id,count:countBySection[s.id]||0})).sort((a,b)=>{
    const ao=sections.find(x=>x.section_id===a.section_id)?.display_order||999;
    const bo=sections.find(x=>x.section_id===b.section_id)?.display_order||999;
    return ao-bo;
  });

  const report={timestamp:new Date().toISOString(),enabled_sections:sections.length,mapped_rows:inserts.length,section_counts:summary};
  const out=path.resolve(process.cwd(),'supabase','industry_category_restructure_report.json');
  fs.writeFileSync(out,JSON.stringify(report,null,2));
  console.log(JSON.stringify(report,null,2));
  console.log('Report:',out);
}

main().catch(e=>{console.error('Failed:',e.message);process.exit(1);});
